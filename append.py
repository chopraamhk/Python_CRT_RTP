"""To add something in the list using append function"""

import sys

def main():
    print("different types of currencies")

    currencies = ['Dollar', 'Euro', 'Pound']

    # append 'Yen' to the list
    currencies.append('Yen')

    print(currencies)
    return 0

if __name__ == "__main__":
    sys.exit=main()
    # Output: ['Dollar', 'Euro', 'Pound', 'Yen']

